package gita;

public class Rectangle {

	public static void main(String[] args) {
		
		int yoko = 10;
		int tate = 20;
		int heri = (yoko + tate)*2;
		int hirosa = yoko * tate;
		
		System.out.println("[結果]\n\n横：" + yoko);
		System.out.println("縦：" + tate);
		System.out.println("へり：" + heri);
		System.out.println("広さ：" + hirosa);
		
	}

}